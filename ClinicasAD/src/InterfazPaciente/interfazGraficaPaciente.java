    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Boton de agregar, este boton agrega el paciente a la base de datos.
        String nombre;
        String apellido;
        int edad;
        String direccion;
        String religion;
        String ocupacion;

        nombre=(String)jTextField1.getText();
        apellido=(String)jTextField2.getText();
        edad = Integer.parseInt(jTextField3.getText());
        direccion=(String)jTextField4.getText();
        religion=(String)jTextField5.getText();
        ocupacion=(String)jTextField6.getText();
        //accion de insertar datos en la base de datos.
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("ClinicasADPU");
        PacienteObjeto nuevoPaciente = new PacienteObjeto();
        PacienteJpaController pc = new PacienteJpaController(emf);

        long ultimo = pc.getPacienteCount();
        System.out.println(ultimo);
        nuevoPaciente.setId(ultimo + 1 );
        nuevoPaciente.setNombre(nombre);
        nuevoPaciente.setApellido(apellido);
        nuevoPaciente.setEdad(edad);
        nuevoPaciente.setDireccion(direccion);
        nuevoPaciente.setReligion(religion);
        nuevoPaciente.setOcupacion(ocupacion);

        pc.create(nuevoPaciente);
        jTextField1.setText(" ");
        jTextField2.setText(" ");
        jTextField3.setText(" ");
        jTextField4.setText(" ");
        jTextField5.setText(" ");
        jTextField6.setText(" ");
        System.out.println(pc.getPacienteCount());
        JOptionPane.showMessageDialog(null, "Paciente ingresado correctamente");

    }//GEN-LAST:event_jButton1ActionPerformed

