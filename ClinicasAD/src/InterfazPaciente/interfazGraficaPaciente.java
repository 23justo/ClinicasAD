    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("ClinicasADPU");
        PacienteJpaController pc = new PacienteJpaController(emf);
        int NumeroPacientes = pc.getPacienteCount();
        int contwhile=0;
        pc.NombresDePacientes();
        pc.ApellidosDePacientes();
        pc.DireccionesDePacientes();
        pc.EdadesDePacientes();
        pc.OcupacionesDePacientes();
        pc.ReligionesDePacientes();

        DefaultTableModel dtm = new DefaultTableModel();
        JTable tabla = this.jTable1;
        tabla.setModel(dtm);
        dtm.setColumnIdentifiers(new Object[]{"ID","APELLIDO","NOMBRE","EDAD","DIRECCION","OCUPACION","RELIGION"});
        try {
            while(contwhile <= NumeroPacientes){
                dtm.addRow(new Object[]{contwhile+1,
                    pc.ApellidosDePacientes().get(contwhile),
                    pc.NombresDePacientes().get(contwhile),
                    pc.EdadesDePacientes().get(contwhile),
                    pc.DireccionesDePacientes().get(contwhile),
                    pc.OcupacionesDePacientes().get(contwhile),
                    pc.ReligionesDePacientes().get(contwhile)});
            contwhile++;
        }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton2ActionPerformed

